package com.lnghealthriskcare.domain;

public class FamilyHistoryDisclosuresIntermediate {
	   private String underwritingSTP;
	   private double debits;
	   private String requirements;
	   private String reasons;
	   
	public String getUnderwritingSTP() {
		return underwritingSTP;
	}
	public void setUnderwritingSTP(String underwritingSTP) {
		this.underwritingSTP = underwritingSTP;
	}
	public double getDebits() {
		return debits;
	}
	public void setDebits(double debits) {
		this.debits = debits;
	}
	public String getRequirements() {
		return requirements;
	}
	public void setRequirements(String requirements) {
		this.requirements = requirements;
	}
	public String getReasons() {
		return reasons;
	}
	public void setReasons(String reasons) {
		this.reasons = reasons;
	}
	   
	   
}
