package com.lnghealthriskcare.domain;

public class HabitDisclosuresIntermediate {
	   private String habitType;
	   private String underwritingSTP;
	   private String ratingType;
	   private String reasons;
	public String getHabitType() {
		return habitType;
	}
	public void setHabitType(String habitType) {
		this.habitType = habitType;
	}
	public String getUnderwritingSTP() {
		return underwritingSTP;
	}
	public void setUnderwritingSTP(String underwritingSTP) {
		this.underwritingSTP = underwritingSTP;
	}
	public String getRatingType() {
		return ratingType;
	}
	public void setRatingType(String ratingType) {
		this.ratingType = ratingType;
	}
	public String getReasons() {
		return reasons;
	}
	public void setReasons(String reasons) {
		this.reasons = reasons;
	}
	   
}
